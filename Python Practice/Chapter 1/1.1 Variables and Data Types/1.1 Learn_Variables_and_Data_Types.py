# What are Variables?
# A variable is a name that refers to a memory location where data is stored. In Python, you don’t need to declare the type of a variable explicitly—it is inferred automatically.

x = 10       # Integer
y = 3.14     # Float
name = "Alice"  # String
is_active = True  # Boolean

# Python Data Types
# Python has several built-in data types:

# Numeric Types: int, float, complex

# Sequence Types: list, tuple, range

# Text Type: str

# Boolean Type: bool

# Set Types: set, frozenset

# Mapping Type: dict

# You can check the type of a variable using type():
print(type(x))   # <class 'int'>
print(type(y))   # <class 'float'>
print(type(name))   # <class 'str'>
print(type(is_active))   # <class 'bool'>
