# Convert a float to an integer and vice versa.
num_float = 5.99
num_int = int(num_float)  # Converts to 5 (removes decimal part)

num_int2 = 8
num_float2 = float(num_int2)  # Converts to 8.0

print(num_int, type(num_int))    # 5 <class 'int'>
print(num_float2, type(num_float2))  # 8.0 <class 'float'>
